SELECT distinct "BRANDS", "YEARS", "COUNTRIES", SUM ("QUANTITY") TOTAL_QUANTITY
FROM BREWERY_DATA
WHERE 
"BRANDS" LIKE '%malt%' and "YEARS" BETWEEN 2018 AND 2019 AND "COUNTRIES" IN ('Nigeria', 'Ghana')
GROUP BY "BRANDS", "YEARS", "COUNTRIES"
ORDER BY "YEARS", SUM("QUANTITY");


