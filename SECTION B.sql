SELECT DISTINCT "BRANDS", SUM("QUANTITY") AS TOTAL_CONSUMPTION
FROM BREWERY_DATA 
WHERE "COUNTRIES" IN ('Togo', 'Senegal', 'Benin') and "YEARS" IN (2017, 2018)
GROUP BY "BRANDS"
order by SUM("QUANTITY") DESC
LIMIT 3;


SELECT DISTINCT "BRANDS", SUM("QUANTITY") AS TOP_BRANDS
FROM BREWERY_DATA 
WHERE "COUNTRIES" = 'Ghana' 
GROUP BY "BRANDS"
ORDER BY SUM("QUANTITY")
LIMIT 1;


